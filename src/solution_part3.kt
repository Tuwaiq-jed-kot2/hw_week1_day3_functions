fun main() {
   println(math(5 , 6))
}
fun math(num1:Int,num2:Int){
    val Add:Int= num1 + num2
    println("Add = $Add")
    val sub:Int= num1 - num2
    println("Subtract = $sub")
    val mult:Int = num1 * num1
    println("Multiply = $mult")
    val div:Int= num1 / num2
    println("Divide = $div")
}
